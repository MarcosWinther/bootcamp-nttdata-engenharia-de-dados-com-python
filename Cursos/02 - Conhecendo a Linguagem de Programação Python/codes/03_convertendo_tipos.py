# Float -> Inteiro
print(int(10.0))

# Inteiro -> Float
print(float(40))

# String -> Inteiro
print(int("20"))

# String -> Float
print(float("20.40"))

# Float -> String
print(str(10.30))

# Descobre o tipo
valor = 80
valor_str = str(valor)
print(type(valor))
print(type(valor_str))

# Divisão
print(100 / 2)
print(100 // 2) # Nesse caso o resultado será só a parte inteira dessa divisão