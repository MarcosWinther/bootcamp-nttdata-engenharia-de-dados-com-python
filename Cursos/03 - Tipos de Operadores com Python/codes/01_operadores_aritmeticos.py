produto_1 = 10
produto_2 = 30

# Adição
print(produto_1 + produto_2)

# Subtração
print(produto_2 - produto_1)

# Multiplicação
print(produto_1 * produto_2)

# Divisão
print(produto_2 / produto_1)

# Divisão inteira
print(produto_2 // produto_1)

# Exponenciação
print(produto_1 ** produto_2)

# Módulo (resto da divisão)
print(produto_1 % produto_2)

# Expressões - analisando a precedência dos operadores
x = 20 + 5 * 4
print(x)

# Reatribuindo outra expressão para a variável x
x = (20 + 5) * 4
print(x)

# Boa prática para escrever operações
y = (2 ** 2) + (10 * 2) - (8 / 2)
print(y)