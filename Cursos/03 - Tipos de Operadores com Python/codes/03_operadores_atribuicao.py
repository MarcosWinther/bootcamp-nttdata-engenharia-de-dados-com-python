# Atribuição simples
valor_1 = 400
print(valor_1)

# Atribuição com adição
valor_1 += 50
print(valor_1)

# Atribuição com subtração
valor_1 -= 50
print(valor_1)

# Atribuição com multiplicação
valor_1 *= 2
print(valor_1)

# Atribuição com divisão
valor_1 /= 2
print(valor_1)

# Atribuição com divisão inteira
valor_1 //= 2
print(valor_1)

# Atribuição com módulo
valor_1 %= 2
print(valor_1)

# Atribuição com exponenciação
valor_1 **= 2
print(valor_1)