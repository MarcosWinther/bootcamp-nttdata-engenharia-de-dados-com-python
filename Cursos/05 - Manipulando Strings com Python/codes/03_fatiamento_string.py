nome = "Marcos Winther Silva Carreiro"

# slice - fatiamento de strings

print(nome[0]) # Informará o caractere do índice 0

print(nome[10]) # Informará o caractere do índice 10

print(nome[-2])

print(nome[7:]) # Informará os caracters a partir do índice 7

print(nome[7:14]) # Informará os caracteres a partir do índice 7 ao índice 14

print(nome[7:14:2]) # Informará os caracteres a partir do índice 7 ao índice 14, mas pulando em 2 índices

print(nome[:]) # Mesmo resultado de print(nome)

print(nome[:: -1]) # Informa de trás para frente