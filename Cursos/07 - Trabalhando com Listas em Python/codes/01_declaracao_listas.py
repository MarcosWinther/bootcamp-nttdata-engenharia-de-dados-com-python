# lista vazia
lista_vazia = []

print(lista_vazia)

# lista utilizando colchetes
animacoes = ["Scooby-Doo", "Batman", "Digimon"]

print(animacoes)

# lista com construtor list
letras = list("python") # cada letra será considerado um valor nessa lista

print(letras)

# lista com função range
numeros = list(range(11))

print(numeros)

# lista com vários tipos de valores
valores_diversos = ["abacaxi", 1996, True]

print(valores_diversos)