lista = ["M", "a", "r", "c", "o", "s"]

print(lista)

# Informa todos os valores da lista
# Mesmo resultado de print(lista)
print(lista[::])

# Informa os valores da lista de forma inversa
print(lista[::-1])

# Vai do índice 2 até o final da lista
print(lista[2:])

# Vai do índice 0 ao índice 1 e desconsidera o índice 2
print(lista[:2])

# Vai do índice 1 ao índice 2 e desconsidera o índice 3
print(lista[1:3])

# Informa o índice 0 e o índice 2 
print(lista[0:3:2])

