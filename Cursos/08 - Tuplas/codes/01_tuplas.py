# Criando tupla utilizando parênteses

animacoes = ("Pokemon", "Digimon", "Batman", "Scooby-Doo",)

print(animacoes)

#---------------------------------------------------------------

# Criando tupla com a classe tuple

letras = tuple("Marcos")

print(letras)

numeros = tuple([10, 20, 30, 40])

print(numeros)

#---------------------------------------------------------------

# Acessando tuplas

## Acesso direito

print(animacoes[3])

## Índices negativos

print(animacoes[-1])